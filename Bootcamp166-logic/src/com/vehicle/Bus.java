package com.vehicle;

public class Bus {

	private String jurusan;
	private String keberangkatan;
	private String tiba;
	private String mrek;
	private String color;
	
	public String getJurusan() {
		return jurusan;
	}
	public void setJurusan(String jurusan) {
		this.jurusan = jurusan;
	}
	public String getKeberangkatan() {
		return keberangkatan;
	}
	public void setKeberangkatan(String keberangkatan) {
		this.keberangkatan = keberangkatan;
	}
	public String getTiba() {
		return tiba;
	}
	public void setTiba(String tiba) {
		this.tiba = tiba;
	}
	public String getMrek() {
		return mrek;
	}
	public void setMrek(String mrek) {
		this.mrek = mrek;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	
	
}
